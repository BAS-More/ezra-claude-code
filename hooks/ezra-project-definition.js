#!/usr/bin/env node
'use strict';
// EZRA Project Definition -- schema validator and serialiser for .ezra/project-definition.yaml
// Zero external dependencies.

const fs   = require('fs');
const path = require('path');

let _yaml;
try { _yaml = require('./ezra-yaml-utils'); } catch (_e) { _yaml = null; }

const MAX_STDIN = 1024 * 1024;

const SCHEMA = {
  required: ['project_name', 'tech_stack', 'description'],
  domains: [
    'project_name', 'description', 'tech_stack', 'features', 'constraints',
    'auth_strategy', 'database_choice', 'deployment_target', 'testing_requirements',
    'security_level', 'standards_level', 'documentation_level', 'rollback_strategy',
    'performance_targets', 'team_size', 'timeline', 'integrations', 'compliance_requirements',
    'ai_tooling', 'phases', 'existing_decisions',
  ],
};

function validate(def) {
  const errors = [];
  for (const req of SCHEMA.required) {
    if (!def[req]) errors.push('Missing required field: ' + req);
  }
  return { valid: errors.length === 0, errors };
}

function serialise(def) {
  if (!_yaml) return JSON.stringify(def, null, 2);
  return _yaml.stringifyYaml(def);
}

function load(projectDir) {
  const p = path.join(projectDir, '.ezra', 'project-definition.yaml');
  if (!fs.existsSync(p)) return null;
  try {
    const text = fs.readFileSync(p, 'utf8');
    if (_yaml) return _yaml.parseYamlSimple(text);
    return JSON.parse(text);
  } catch (_e) { return null; }
}

function save(projectDir, def) {
  const dir = path.join(projectDir, '.ezra');
  if (!fs.existsSync(dir)) fs.mkdirSync(dir, { recursive: true });
  const p = path.join(dir, 'project-definition.yaml');
  const text = '# EZRA Project Definition\n# Generated by /ezra:interview\n\n' + serialise(def);
  fs.writeFileSync(p, text, 'utf8');
  return p;
}

module.exports = { validate, serialise, load, save, SCHEMA };

if (require.main === module) {
  let input = '';
  process.stdin.setEncoding('utf8');
  process.stdin.on('data', chunk => { input += chunk; if (input.length > MAX_STDIN) process.exit(0); });
  process.stdin.on('end', () => {
    try {
      const ev = JSON.parse(input);
      const cwd = ev.cwd || process.cwd();
      const def = load(cwd);
      if (def) {
        const { valid, errors } = validate(def);
        process.stdout.write(JSON.stringify({ valid, errors, definition: def }));
      } else {
        process.stdout.write(JSON.stringify({ valid: false, errors: ['No project-definition.yaml found'] }));
      }
    } catch (_e) { process.stdout.write('{}'); }
    process.exit(0);
  });
}
